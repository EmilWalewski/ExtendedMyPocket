package com.mypocket.storeManagement.utilities;

public class ReceiptDate {

    private String date;
    private String time;

    public ReceiptDate() {
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
